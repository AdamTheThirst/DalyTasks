from collections import defaultdict

d = {"a": 1, "b": 2, "c": 1}

def invert_dict(d: dict) -> dict:
    if d:
        reversed = defaultdict(list)
        for k, v in d.items():
            reversed[v].append(k)

        return reversed
    return {}

print(invert_dict(d))